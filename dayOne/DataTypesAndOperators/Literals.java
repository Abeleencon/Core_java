package dayOne.DataTypesAndOperators;

public class Literals {

	//Literals means the value that you assign to variables in java in different numebering system
	
	public static void main(String[] args) {
		
		int a = 101;			//Decimal
		int b = 0100;			//Octal
		int c = 0xFace;			//Hexadecimal
		int d = 0b11101011;    //Binary
		double e = 001234.4334;
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
	}
}
